var expanded = false;

function keyPartners() {
    var keyPartners = document.getElementById("keyPartners");
    if (!expanded) {
        keyPartners.style.display = "block";
        expanded = true;
    } else {
        keyPartners.style.display = "none";
        expanded = false;
    }
}
